package com.eomcs.lang.ch02;

public class e06연산자 {
  public static void main (String[] args) {

    boolean b = true;
    System.out.println(b);

    System.out.println(true && false);
    System.out.println(true || false); //| :필터키 || : OR연산자

    System.out.println((10 == 20) || (30!=40));
  }

}
